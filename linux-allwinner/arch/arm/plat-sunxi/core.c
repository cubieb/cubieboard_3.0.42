enum sw_ic_ver sw_get_ic_ver(void)
{
	volatile u32 val = readl(SW_VA_TIMERC_IO_BASE + 0x13c);

	val = (val >> 6) & 0x3;

	if (machine_is_sun4i()) {
		switch (val) {
		case 0x00:
			return MAGIC_VER_A;
		case 0x03:
			return MAGIC_VER_B;
		default:
			return MAGIC_VER_C;
		}
	} else {
		switch (val) {
		case 0x03:
			return MAGIC_VER_B;
		default:
			return MAGIC_VER_A;
		}
	}
}
